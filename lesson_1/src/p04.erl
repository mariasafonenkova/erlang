%% ====================================================================
%% API functions
%% ====================================================================
-export([len/1, len1/1]).
len(L) ->
	len(L,0).

len([_H|T], N) ->
	len(T, N+1);
len([], N) ->
	N.

len1([_|T]) ->
	1 + len(T);
len1([]) -> 0.


