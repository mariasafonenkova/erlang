-module(p13).

%% ====================================================================
%% API functions
%% ====================================================================
-export([decode/1]).
decode([]) ->
	[];
decode([{1,H}|T]) ->
	decode([H|T]);
decode([{2,H}|T]) ->
	decode([H|[H|T]]);
decode([{N,H}|T]) ->
	decode([{N-1,H}|[H|T]]);
decode([H|T]) ->
	[H|decode(T)].
	



